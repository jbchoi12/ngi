package egovframework.let.uat.sso.service;

public class SSOVO {

	
	private String sUid;
	private String sUserNm;
	private String sEmail;
	private String sTel;
	private String sAddr;
	private String sDtlAddr;
	private String sOrganNm;
	
	
	public String getsUid() {
		return sUid;
	}
	public void setsUid(String sUid) {
		this.sUid = sUid;
	}
	public String getsUserNm() {
		return sUserNm;
	}
	public void setsUserNm(String sUserNm) {
		this.sUserNm = sUserNm;
	}
	public String getsEmail() {
		return sEmail;
	}
	public void setsEmail(String sEmail) {
		this.sEmail = sEmail;
	}
	public String getsTel() {
		return sTel;
	}
	public void setsTel(String sTel) {
		this.sTel = sTel;
	}
	public String getsAddr() {
		return sAddr;
	}
	public void setsAddr(String sAddr) {
		this.sAddr = sAddr;
	}
	public String getsDtlAddr() {
		return sDtlAddr;
	}
	public void setsDtlAddr(String sDtlAddr) {
		this.sDtlAddr = sDtlAddr;
	}
	public String getsOrganNm() {
		return sOrganNm;
	}
	public void setsOrganNm(String sOrganNm) {
		this.sOrganNm = sOrganNm;
	}	
	

}
